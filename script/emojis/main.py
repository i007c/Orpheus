font_path = '/usr/share/fonts/noto/NotoColorEmoji.ttf'
ALLE = [0]


def get_gidmap(ttf, file):
    lookup_list = ttf['GSUB'].table.LookupList.Lookup
    cmap = {v: k for k, v in ttf.getBestCmap().items()}
    gmap = {}
    output = {}

    for i, v in enumerate(ttf.getGlyphOrder()):
        gmap[v] = i
        if v in cmap:
            output[i] = chr(cmap[v])

    for ll in lookup_list:
        t = ll.SubTable[0]
        if not hasattr(t, 'ligatures'):
            continue

        for c, es in t.ligatures.items():
            for k in es:
                # gid = ttf.getGlyphID(k.LigGlyph)
                e = chr(cmap[c])
                e += ''.join(map(lambda j: chr(cmap[j]), k.Component))
                output[gmap[k.LigGlyph]] = e

    file.write('\n\nconst char *gidmap[4000] = {\n')
    for g in sorted(output.items(), key=lambda x: x[0]):
        file.write(f'    [{g[0]}] = "{g[1]}",\n')
    file.write('}\n\n')


def get_nonfound(ttf, file):
    lookup_list = ttf['GSUB'].table.LookupList.Lookup
    cmap = {v: k for k, v in ttf.getBestCmap().items()}
    gmap = {}
    output = {}

    for i, v in enumerate(ttf.getGlyphOrder()):
        gmap[v] = i
        if v in cmap and i not in ALLE:
            output[i] = chr(cmap[v])

    for ll in lookup_list:
        t = ll.SubTable[0]
        if not hasattr(t, 'ligatures'):
            continue

        for c, es in t.ligatures.items():
            for k in es:
                # gid = ttf.getGlyphID(k.LigGlyph)
                gid = gmap[k.LigGlyph]
                if gid in ALLE:
                    continue
                e = chr(cmap[c])
                e += ''.join(map(lambda j: chr(cmap[j]), k.Component))
                output[gmap[k.LigGlyph]] = e

    file.write('\n\nconst FT_UInt e_flags[] = {\n')
    for g in sorted(output.items(), key=lambda x: x[0]):
        file.write(f'    {g[0]}, // |{g[1]}|\n')
    file.write('}\n\n')


